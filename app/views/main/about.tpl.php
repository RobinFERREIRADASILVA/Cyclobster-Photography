            <h2>Qui suis-je ?</h2>
            <hr>
            <p>La réalité n’est jamais ce que l’on perçoit, chaque regard est différent. Tout dépend de comment le regarde se porte sur telle ou telle chose. Les formes, les courbes, la lumière. Nous sommes tous des artistes.</p>
            <p>Bonjour à vous tous, je suis <strong>Mickaël </strong>(<em>Cyclobster</em>) photographe depuis 2019. Tout d’abord j’avais acheté un appareil photo numérique pour faire de la vidéo sur internet, sans me préoccuper de l’intérêt photographique de l’objet. Quelques photos par ci par là sans plus d’intérêt aucun. Puis 2020 et ses événements m’ont poussé a faire de la photographie et beaucoup (BEAUCOUP) moins de vidéos. C’est à ce moment précis que le déclic est apparu. Je ne vivais désormais plus que pour et par la photo. En Juillet 2020 je décide donc de me mettre à l’argentique pour mon plus grand plaisir. </p>
            <p>Aujourd’hui encore je pousse et repousse ma vision de la photo, de mon environnement et de mes sujets pour vous apporter une vision unique de chaque personne/architecture/foret et autres contextes naturels ou non. J’aime l’idée que j’essaye de faire sortir le plus beaux de chaque sujet, avec un regard tendre et bienveillant. 
            <p><strong>La photo c’est de l’art a portée de tous</strong></p>
            <p>Pour moi la photo c’est avant tout une histoire d’amour et de tendresse. </p>
            <p><em style="font-size:1.2em">« Les photos sont là, et il ne te reste plus qu’à les prendre. »</em> Robert Capa</p>
            
